                                <h1>
                                    <b><i>War Mage:</i>&nbsp;</b><b>Profession &ndash; Clergy</b>
                                </h1>
                                <p>The War Mage is a member of the battle caste of the Clergy. In addition to powerful magical arts, the War Mage can wield Short Swords, Daggers, and Slings, along with Grenade Weapons, and imbue them with enchantments.
                                </p>
                                <h1>
                                    <b><i>Ballast Knight:</i> Profession &ndash; Noble</b>
                                </h1>
                                <p>The Ballast Knight is part of the Nobility, and heralds from the upper elite of society. This access to wealth allows them to bear costly yet powerful weapons and armor. Some of the weapons the Ballast Knight wields are the Great Sword, Short Sword, Axe, and for mounted knights, the Lance.

                                </p>
                                <h1>
                                    <b><i>Polearm Knight:</i> Profession &ndash; Pioneer</b>
                                </h1>
                                <p>A Polearm Knight is the upper echelon of the rank and file, the commoners who make up the majority of the Rasfalian cultures. A fierce and heroic fighter, Polearm Knights are known to wield Pikes, Halberds, Scythes, and other pole weapons.
                                </p>
                                <h1>
                                    <b><i>Aerolest:</i> Profession &ndash; Estate</b>
                                </h1>
                                <p>The Aerolest is a highly skilled, highly educated warrior, immersed in mathematics and the sciences. Known to design and develop their own secretive and extremely deadly ranged weapons, the Aerolest uses various types of Bows, Crossbows, and Scorpios.</p>
