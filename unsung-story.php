                                <p><i>Unsung Story</i> is an exciting turn-based, tactical war simulation RPG that plays across a great epoch, through individual story episodes. <b>YOU,</b> <b>the player,</b> research and explore the vast history of this conflict that nearly tore Rasfalia apart, by reliving past battles and political events.&nbsp;</p>
                                <p><b>Your viewpoint into this series of intertwined history varies from battle to battle.</b> In one fight, you may command a group of knights protecting a great empire, while in another scenario you may lead a group of revolutionary soldiers, made up of commoners, who are fighting to topple the same empire!</p>
                                <p>Battles take place on a <b>3-D landscape,</b> with an <b>immersive player controlled camera,</b> while episode and scene selection is made through a detailed 3-D world map. Units that you command are profession based, and within each profession are multiple classes.&nbsp;</p>
                                <p><b>Each class within a profession has certain characteristics, and you can change classes within a profession to suit your tactical needs!&nbsp;</b>The world is also rich with magical creatures that are not only foes on the battlefield, but under certain conditions can be commanded by you as well!</p>
                                <p>The character professions, replete with individual classes, provide you with deep game mechanics that you can <b>mold to your individual play style, </b>and <b>adapt to the scenario at hand. You will build your front and backlines by combing these classes into various groups or parties, based on the needs of the battlefield. </b></p>

