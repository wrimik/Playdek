                        <p></p><p>Video game development entails risk. All developers are daredevils at heart BUT we do all we can to lessen the risk. Like starting with an amazing idea, partnering with one of the greatest designers in the history of the industry, throwing in fantastic music and features, then run it all through a developer with a solid track record of performance on every major video game console… You get the idea.</p>

